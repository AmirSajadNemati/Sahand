class StoryDeleteView(APIView):

    @extend_schema(
        request=DeleteSerializer,  # The request body will contain the `id` of the story
        responses={200: OpenApiResponse(response=MessageAndIdSerializer)},
        description="Delete a specific Story by ID"
    )
    @role_decorator
    def post(self, request):
        story_id = request.data.get('id')
        delete_type = request.data.get('type')

        if not story_id:
            return Response({"message": "شناسه مورد نیاز است."}, status=status.HTTP_400_BAD_REQUEST)

        try:
            story = Story.objects.get(id=story_id)
        except Story.DoesNotExist:
            return Response({"message": "داستان یافت نشد!"}, status=status.HTTP_400_BAD_REQUEST)

        if delete_type == 1:
            # Soft delete: Set is_deleted to True
            if story.is_deleted:
                return Response({"message": "داستان از قبل حذف نرم شده است."}, status=status.HTTP_400_BAD_REQUEST)
            story.is_deleted = True
            story.save()
            return Response({"message": "داستان به صورت نرم پاک شد."}, status=status.HTTP_200_OK)

        elif delete_type == 2:
            # Soft delete the associated photo if it exists
            if story.is_deleted:
                return Response({"message": "داستان از قبل حذف نرم شده است."}, status=status.HTTP_400_BAD_REQUEST)
            if story.photo:
                story.photo.is_deleted = True
                story.photo.save()

            # Soft delete the story itself
            story.is_deleted = True
            story.save()
            return Response({"message": "داستان و عکس آن به صورت نرم پاک شدند."}, status=status.HTTP_200_OK)

        elif delete_type == 3:
            # Hard delete: Remove the record from the database
            try:
                story.delete()
                return Response({"message": "داستان به صورت فیزیکی پاک شد."}, status=status.HTTP_200_OK)
            except Exception as e:
                return Response({"message": f"حذف ناموفق: {str(e)}"}, status=status.HTTP_400_BAD_REQUEST)

        elif delete_type == 4:
            # Hard delete with dependencies
            try:
                # Manually delete the associated photo if it exists
                if story.photo:
                    story.photo.delete()

                # Finally, delete the story itself
                story.delete()
                return Response({"message": "داستان و تمام وابستگی‌های آن به صورت فیزیکی پاک شدند."}, status=status.HTTP_200_OK)
            except Exception as e:
                return Response({"message": f"حذف ناموفق: {str(e)}"}, status=status.HTTP_400_BAD_REQUEST)

        else:
            return Response({"message": "نوع حذف نا معتبر."}, status=status.HTTP_400_BAD_REQUEST)


class StoryUndeleteView(APIView):

    @extend_schema(
        request=DeleteSerializer,  # The request body will contain the `id` of the story
        responses={200: OpenApiResponse(response=MessageAndIdSerializer)},
        description="Restore a soft-deleted Story by ID"
    )
    @role_decorator
    def post(self, request):
        story_id = request.data.get('id')
        delete_type = request.data.get('type')

        if not story_id:
            return Response({"message": "شناسه مورد نیاز است."}, status=status.HTTP_400_BAD_REQUEST)

        try:
            story = Story.objects.get(id=story_id)
        except Story.DoesNotExist:
            return Response({"message": "داستان یافت نشد!"}, status=status.HTTP_400_BAD_REQUEST)

        if story.is_deleted:
            # If the story is soft-deleted, restore it by setting is_deleted to False
            if delete_type == 1:
                story.is_deleted = False
                story.save()
                return Response({"message": "داستان به صورت نرم بازیابی شد."}, status=status.HTTP_200_OK)

            elif delete_type == 2:
                if story.photo:
                    story.photo.is_deleted = False
                    story.photo.save()

                story.is_deleted = False
                story.save()
                return Response({"message": "داستان و عکس آن به صورت نرم بازیابی شدند."}, status=status.HTTP_200_OK)

            else:
                return Response({"message": "نوع بازیابی نا معتبر."}, status=status.HTTP_400_BAD_REQUEST)
        else:
            return Response({"message": "داستان مورد نظر حذف نشده است."}, status=status.HTTP_400_BAD_REQUEST)

