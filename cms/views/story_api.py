class StoryGetListView(APIView):

    @extend_schema(
        request=ListRequestSerializer,
        responses={
            200: OpenApiResponse(response=StoryListSerializer()),
            400: OpenApiResponse(description='Bad Request')
        },
        description="Get List of Stories"
    )
    @role_decorator
    def post(self, request):
        # Validate and deserialize the request data
        serializer = ListRequestSerializer(data=request.data)
        if not serializer.is_valid():
            return Response(serializer.errors, status=status.HTTP_400_BAD_REQUEST)

        data = serializer.validated_data
        sort = data.get('sort', '')
        page = data.get('page', 1)
        page_size = data.get('pageSize', 10)
        is_deleted = data.get('is_deleted', False)
        searches = data.get('searches', [])
        filters = data.get('filters', [])

        # Build query
        query = Q(is_deleted=is_deleted)

        # Apply filters
        for filter_item in filters:
            column = filter_item['column']
            value = filter_item['value']
            query &= Q(**{f"{column}__iexact": value})

        # Apply searches
        for search_item in searches:
            column = search_item['column']
            value = search_item['value']
            query &= Q(**{f"{column}__icontains": value})

        stories = Story.objects.filter(query)

        # Sorting
        if sort:
            sort_field = sort if sort.startswith('') else f'-{sort}'
            stories = stories.order_by(sort_field)
        else:
            # Apply a default ordering (e.g., by 'id' or another field)
            stories = stories.order_by('id')

        # Pagination
        paginator = StoryPagination()
        paginated_stories = paginator.paginate_queryset(stories, request)

        # Serialize the paginated data
        serializer = StorySerializer(paginated_stories, many=True)

        # Return paginated response
