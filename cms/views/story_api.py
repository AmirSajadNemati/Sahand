class StoryAddOrUpdateView(APIView):

    @extend_schema(
        request=StorySerializer,
        responses={
            200: OpenApiResponse(response=MessageAndIdSerializer),
        },
        description="Add or Update Story"
    )
    @role_decorator
    def post(self, request):
        story_id = request.data.get('id', 0)

        # If story_id is 0, it's a new story, so create it
        if story_id == 0:
            serializer = StorySerializer(data=request.data)
            if serializer.is_valid():
                serializer.save()
                return Response({"message": "داستان با موفقیت ایجاد شد!", "id": serializer.instance.id}, status=status.HTTP_200_OK)
            return Response(serializer.errors, status=status.HTTP_400_BAD_REQUEST)

        # If story_id is provided, try to update the existing story
        try:
            story = Story.objects.get(pk=story_id)
        except Story.DoesNotExist:
            return Response({'message': 'داستان مورد نظر برای تغییر یافت نشد.', "id": story_id},
                            status=status.HTTP_400_BAD_REQUEST)

        # Update the existing story
        serializer = StorySerializer(story, data=request.data, partial=True)
        if serializer.is_valid():
            serializer.save()
            return Response({"message": "داستان با موفقیت به روزرسانی شد!", "id": story_id}, status=status.HTTP_200_OK)
        return Response(serializer.errors, status=status.HTTP_400_BAD_REQUEST)


